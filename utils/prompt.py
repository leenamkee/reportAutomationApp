def generate_report_prompt(context: str, guide: str) -> str:
    prompt = f"""
당신은 베테랑 전략기획팀 출신의 전문가로서, 경영진에게 보고할 수준의 명확하고 간결한 업무보고서를 작성하는 역할을 맡고 있습니다.

다음 내용을 기반으로 다음의 항목을 포함한 구조적 보고서를 작성해 주세요:

1. 업무 배경
2. 주요 문제점
3. 해결 방안
4. 향후 계획

보고서는 다음 조건을 충족해야 합니다:
- 내용이 명확하고 중복 없이 간결할 것
- 항목별 소제목을 유지할 것
- 분석 중심의 문체(감정 없이 사실 기반)
- 문서에서 추론 가능한 정보만 사용할 것

사용자가 작성한 보고서 작성 방향 가이드는 다음과 같습니다:
{guide}

사용자가 참고 문서로 제공한 내용은 다음과 같습니다:

{context}

위 내용을 기반으로 전체 보고서를 작성해 주세요.
"""
    return prompt